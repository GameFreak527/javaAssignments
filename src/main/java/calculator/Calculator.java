package calculator;

public class Calculator {
	
	public long addNumbers(long a, long b) {
		return a + b;
	}
	
	public long subtractNumbers(long a, long b) {
		return a - b;
	}
	
	public long multiplyNumbers(long a , long b) {
		return  a * b;
	}
	
	public long divideNumbers(long a, long b) {
		return a / b;
	}
}
